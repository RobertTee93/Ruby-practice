# Prints the classic song!

bottles = 99

while bottles <= 99 && bottles > 1
  puts "#{bottles} Bottles of beer on the wall, #{bottles} Bottles of beer! Take one down and pass it around..."
  bottles -= 1
end

puts "1 Bottle of beer on the wall, 1 bottle of beer, take it down and pass it around..."

puts "No more bottles of beer on the wall, no more bottles of beer... go to the store and buy some more. 99 bottles of beer!"
